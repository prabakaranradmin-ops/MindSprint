/* Regenerates sw.js's precache list from the real file set (§2 offline-first,
   §19.1 config-driven). Phoneme clip filenames come from content.js's
   phonemeIds (§15.3) so the list can't drift from the actual manifest.
   Run: node scripts/gen-service-worker.cjs (re-run whenever phonemeIds,
   vendor/, or the core app files change). */
const fs = require('fs');
const path = require('path');
const loadContent = require('../content.js.export.cjs');

const CACHE_VERSION = process.argv[2] || 'v1';
const phonemeIds = loadContent().phonemeIds;

const CORE_FILES = [
  './',
  'app.html',
  'content.js',
  'audio-manager.jsx',
  'styles.css',
  'manifest.webmanifest',
  'vendor/react.development.js',
  'vendor/react-dom.development.js',
  'vendor/babel.min.js',
  'assets/icon-192.png',
  'assets/icon-512.png',
  'assets/fonts/fredoka-variable.woff2',
  'assets/fonts/nunito-variable.woff2',
];

const phonemeFiles = phonemeIds.flatMap(id => [
  `assets/phonemes/${id}.wav`,
]);

const ALL_FILES = [...CORE_FILES, ...phonemeFiles];

const swSrc = `/* GENERATED by scripts/gen-service-worker.cjs — do not hand-edit the
   PRECACHE_URLS list, re-run the script instead (source: content.js
   phonemeIds + the core file set hardcoded in the generator).
   §2 offline-first / §17.1 test 7: the whole app must work with no network. */
const CACHE_NAME = 'bloom-academy-${CACHE_VERSION}';
const PRECACHE_URLS = ${JSON.stringify(ALL_FILES, null, 2)};

self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then((cache) => cache.addAll(PRECACHE_URLS))
      .then(() => self.skipWaiting())
  );
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((keys) =>
      Promise.all(keys.filter((k) => k !== CACHE_NAME).map((k) => caches.delete(k)))
    ).then(() => self.clients.claim())
  );
});

// Cache-first: the game must work with networking disabled (§17.1 test 7).
// Falls back to network for anything not precached, and silently ignores
// fetch failures rather than surfacing an error to the child (§22.2).
self.addEventListener('fetch', (event) => {
  if (event.request.method !== 'GET') return;
  event.respondWith(
    caches.match(event.request).then((cached) => {
      if (cached) return cached;
      return fetch(event.request).then((res) => {
        if (res && res.ok) {
          const copy = res.clone();
          caches.open(CACHE_NAME).then((cache) => cache.put(event.request, copy));
        }
        return res;
      }).catch(() => cached);
    })
  );
});
`;

fs.writeFileSync(path.join(__dirname, '..', 'sw.js'), swSrc);
console.log(`Wrote sw.js — cache "${CACHE_VERSION}", ${ALL_FILES.length} precached files (${phonemeFiles.length} phoneme clips).`);
